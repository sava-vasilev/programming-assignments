passwordRegex = r"^(?=.*?[A-Z])(?=.*?[a-z])(?=.*?[0-9])(?=.*?[#?!@$%^&*-]).{8,}$"
nameRegex = r"/^[a-zA-ZàáâäãåąčćęèéêëėįìíîïłńòóôöõøùúûüųūÿýżźñçčšžÀÁÂÄÃÅĄĆČĖĘÈÉÊËÌÍÎÏĮŁŃÒÓÔÖÕØÙÚÛÜŲŪŸÝŻŹÑßÇŒÆČŠŽ∂ð ,.'-]+$/u"


# Define the checkEmail method
def checkEmail(email):
    # Check if email is valid
    if re.fullmatch(emailRegex, email):
        # Return True
        return True
    else:
        # Return False
        return False


# Define the checkPassword method
def checkPassword(password1, password2):
    # Check if password is valid and matches the confirmation
    if re.fullmatch(passwordRegex, password1) and password1 == password2:
        # Return True
        return True
    else:
        # Return False
        return False


# Define the checkName method
def checkName(name):
    # Check if there is a name
    if len(name) > 0:
        # Return True
        return True
    # Else check if name is valid
    elif re.fullmatch(nameRegex, name):
        # Return True
        return True
    else:
        # Return False
        return False
