class Register:

    def __init__(self, reg_firstname, reg_lastname, reg_username, reg_password, reg_emailaddress, reg_phone):
        self.reg_firstname = reg_firstname
        self.reg_lastname = reg_lastname
        self.reg_username = reg_username
        self.reg_emailaddress = reg_emailaddress
        self.reg_password = reg_password
        self.reg_phone = reg_phone
